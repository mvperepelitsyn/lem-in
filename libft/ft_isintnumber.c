#include "libft.h"

int	ft_isintnumber(char *c)
{
	int			i;
	long int	ch;

	i = ft_strlen(c);
	ch = ft_latoi(c);
	if (ch > 2147483647 || ch < -2147483648)
		return (0);
	if (*c == '-' && i != 1)
		c++;
	while (i > 0)
	{
		if (!(*c >= 48 && *c <= 57) && *c != '\0')
			return (0);
		c++;
		i--;
	}
	return (1);
}
